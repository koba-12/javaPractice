package day08_IfStatements;

public class SingleIfStatements2 {
    public static void main(String[] args) {

        //multi-branch

        int n = 200;

        //positive
        if(n > 0){
            System.out.println(n+ " is positive");
        }
        //negative
        if (n < 0){
            System.out.println(n + " is positive");
        }
        //zero
        if (n == 0){
            System.out.println(n + " is zero ");
        }
    }
}
