package day15_ForLoop;

public class ForLoopIntro {

    public static void main(String[] args) {


        System.out.println("Hello World!");
        System.out.println("Hello World!");
        System.out.println("Hello World!");
        System.out.println("Hello World!");
        System.out.println("Hello World!");
        System.out.println("Hello World!");
        System.out.println("Hello World!");
        System.out.println("Hello World!");
        System.out.println("Hello World!");
        System.out.println("Hello World!");
        System.out.println("Hello World!");


        System.out.println("-------------------------------------------------");

        // initialization;condition;iteration
        for (int i = 1; i<=10; i++){// böyle yazınca sanırım i<=10 olduğu için 10 tane hello world çıkıyor.ve evet buraya i++ yazmalıyız
            System.out.println("Hello World!");//kesin bilgi < işareti varsa ++,,,> işareti varsa -- kullanacağız
        }


    }
}
